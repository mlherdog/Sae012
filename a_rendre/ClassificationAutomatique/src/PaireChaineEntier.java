public class PaireChaineEntier {
    // attribut
    private String chaine;
    private int entier;

    // constructeur
    public PaireChaineEntier(String chaine,int entier){
        this.chaine = chaine;
        this.entier = entier;
    }

    //getteur

    public String getChaine() {
        return chaine;
    }

    public int getEntier() {
        return entier;
    }
}
